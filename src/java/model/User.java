package model;

import java.io.Serializable;

/**
 *
 * @author ADMIN
 */
public class User implements Serializable{
    private int ID;
    private String ho,ten,gender,dob,email,password;

    public User() {
    }

    public User(int ID, String ho, String ten, String gender, String dob, String email, String password) {
        this.ID = ID;
        this.ho = ho;
        this.ten = ten;
        this.gender = gender;
        this.dob = dob;
        this.email = email;
        this.password = password;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getHo() {
        return ho;
    }

    public void setHo(String ho) {
        this.ho = ho;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    
}
